package Model;

public class Filial {
  private Integer id_filial;
  private String nome;

  public Integer getId() {
    return id_filial;
  }

  public void setId(Integer id) {
    this.id_filial = id;
  }

  public String getNome() {
    return nome;
  }

  public void setNome(String nome) {
    this.nome = nome;
  }
}
