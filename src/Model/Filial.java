package Model;

public class Filial {
	private Integer id_filial;
	private String nome_filial;
	private String localidade;
	
	public Integer getId_filial() {
		return id_filial;
	}
	public void setId_filial(Integer id_filial) {
		this.id_filial = id_filial;
	}
	public String getNome_filial() {
		return nome_filial;
	}
	public void setNome_filial(String nome_filial) {
		this.nome_filial = nome_filial;
	}
	public String getLocalidade() {
		return localidade;
	}
	public void setLocalidade(String localidade) {
		this.localidade = localidade;
	}
}
